# Test 4: Built-in Functions
print("=== Test 4: Built-in Functions ===")

# Type conversion
x = "42"
print("str to int:", int(x))

y = 3.14
print("float to int:", int(y))

z = 10
print("int to float:", float(z))
print("int to str:", str(z))
print("int to bool:", bool(z))

# len() function
text = "Hello"
print("len('Hello'):", len(text))

# type() function
print("type(42):", type(42))
print("type(3.14):", type(3.14))
print("type('hello'):", type("hello"))
print("type(True):", type(True))

print("\n=== Test 4 Complete ===")
