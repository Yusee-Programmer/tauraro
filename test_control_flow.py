# Test control flow structures
x = 5
y = 10

# Test if statement
if x < y:
    print("x is less than y")
else:
    print("x is not less than y")

# Test while loop
i = 0
while i < 3:
    print("i =", i)
    i = i + 1

# Test for loop with range
for j in range(3):
    print("j =", j)

print("Done!")
