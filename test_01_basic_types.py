# Test 1: Basic Data Types and Operations
print("=== Test 1: Basic Data Types ===")

# Integers
x = 42
print("Integer x =", x)

# Floats
y = 3.14
print("Float y =", y)

# Strings
name = "Tauraro"
print("String name =", name)

# Booleans
is_true = True
is_false = False
print("Boolean is_true =", is_true)
print("Boolean is_false =", is_false)

# None
nothing = None
print("None value =", nothing)

# Arithmetic operations
a = 10
b = 3
print("\nArithmetic Operations:")
print("10 + 3 =", a + b)
print("10 - 3 =", a - b)
print("10 * 3 =", a * b)
print("10 / 3 =", a / b)
print("10 % 3 =", a % b)

# Comparison operations
print("\nComparison Operations:")
print("10 == 3:", a == b)
print("10 != 3:", a != b)
print("10 > 3:", a > b)
print("10 < 3:", a < b)
print("10 >= 3:", a >= b)
print("10 <= 3:", a <= b)

print("\n=== Test 1 Complete ===")
