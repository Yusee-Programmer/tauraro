# Test 5: Control Flow
print("=== Test 5: Control Flow ===")

# If-else
x = 10
if x > 5:
    print("x is greater than 5")

if x < 5:
    print("x is less than 5")
else:
    print("x is not less than 5")

# While loop
count = 0
print("\nWhile loop:")
while count < 3:
    print("Count:", count)
    count = count + 1

# For loop with range
print("\nFor loop:")
for i in range(3):
    print("Iteration:", i)

print("\n=== Test 5 Complete ===")
